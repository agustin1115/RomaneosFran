# CALIFICACIÓN DE NEGOCIO
# ══════════════════════════════════════════════════════════════════════
def calificacion_negocio(margen_pct):
    """Califica el resultado del negocio por margen %."""
    if margen_pct < 0:
        return 'PÉRDIDA'
    elif margen_pct >= 15:
        return 'ÓPTIMO'
    elif margen_pct >= 8:
        return 'BUENO'
    elif margen_pct >= 3:
        return 'REGULAR'
    else:
        return 'MALO'

def calificacion_corte(cumplimiento_pct, es_caro):
    """Califica rendimiento de un corte. Lógica invertida para baratos."""
    if es_caro:
        if cumplimiento_pct >= 1.10: return 'ÓPTIMO'
        if cumplimiento_pct >= 0.95: return 'BUENO'
        if cumplimiento_pct >= 0.80: return 'REGULAR'
        return 'MALO'
    else:
        if cumplimiento_pct <= 0.80: return 'ÓPTIMO'
        if cumplimiento_pct <= 0.95: return 'BUENO'
        if cumplimiento_pct <= 1.10: return 'REGULAR'
        return 'MALO'
