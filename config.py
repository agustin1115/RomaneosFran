# NETOS PEYA = PEDIDOS YA lista - 5% logística - 2% merma - 4.5% comisión
# ══════════════════════════════════════════════════════════════════════
DESCUENTO_PEYA_LOGISTICA = 0.05
DESCUENTO_PEYA_MERMA = 0.02
DESCUENTO_PEYA_COMISION = 0.045

def calcular_neto_peya(precio_lista):
    """Calcula precio neto PEYA desde precio de lista."""
    if not precio_lista or precio_lista <= 0:
        return 0
    return round(precio_lista * (1 - DESCUENTO_PEYA_LOGISTICA)
                               * (1 - DESCUENTO_PEYA_MERMA)
                               * (1 - DESCUENTO_PEYA_COMISION))

